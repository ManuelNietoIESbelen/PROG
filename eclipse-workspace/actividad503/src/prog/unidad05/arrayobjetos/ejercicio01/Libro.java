package prog.unidad05.arrayobjetos.ejercicio01;

public class Libro {

  private String titulo;
  private String autor;
  private int anyo;
  
  
  
   
  public Libro(String titulo, String autor, int anyo) {
    this.titulo = titulo;
    this.autor = autor;
    this.anyo = anyo;
  }


  public String getTitulo() {
    return titulo;
  }

  public String getAutor() {
    return autor;
  }


  public int getAnyo() {
    return anyo;
  }


  





  
  
  }

